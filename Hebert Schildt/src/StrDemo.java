/*
    Demonstra a sequencia de escape em strings.
 */
public class StrDemo {
    public static void main(String[] args) {
        System.out.println("Primeira linha \nSegunda linha");  // <-- Usa \n para gerar uma nova linha
        System.out.println("A\tB\tC");
        System.out.println("D\tE\tF");

        //^-- Usa tabulação para alinhar a saída
    }
}
