public class Escada {
    public static void main(String[] args) {
        int x;

        for(x = 0; x < 6; x ++) {
            if (x == 1)
                System.out.println(" X é um");
            else if (x == 2)
            System.out.println("X é dois");
            else if (x == 3)
            System.out.println("X é três");
            else if (x == 4)
            System.out.println("X é quatro");
            else
            System.out.println("X não esta entre 1 e 4");
        }
    }
}